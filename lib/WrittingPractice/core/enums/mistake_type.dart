///Enumerate several mistake types
enum MistakeType {
  /// Misspelling mistake type
  misspelling,

  /// Typographical mistake type
  typographical,

  /// Grammar mistake type
  grammar,

  /// Uncategorized mistake type
  uncategorized,

  /// NonConformance mistake type
  nonConformance,

  /// Style mistake type
  style,

  /// Any other mistake type
  other,
}
